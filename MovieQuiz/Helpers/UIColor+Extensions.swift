import Foundation
import UIKit

extension UIColor {
    
    class var ypGreen:UIColor {
        return UIColor(red: 0x60/255.0, green: 0xC2/255.0, blue: 0x8E/255.0, alpha: 1.0)
    }
    class var ypRed:UIColor {
        return UIColor(red: 0xF5/255.0, green: 0x6B/255.0, blue: 0x6C/255.0, alpha: 1.0)
    }
}
